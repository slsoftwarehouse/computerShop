    void searchSupplier(String searchString, String paraName, String paraValue) {
        Query createQuery = em.createQuery(ENTITY_QUERY + searchString);
        createQuery.setParameter(paraName, paraValue);
        createQuery.setMaxResults(1);
        Entity entity = (Entity) createQuery.getSingleResult();
        selectedSupplier = entity;
        txtSupplier.setText(entity.getName());
    }


