    void loadGRNLinesTable(String serial) {

        DefaultTableModel tblModel = (DefaultTableModel) tblSub.getModel();
        tblModel.setRowCount(0);
        for (GrnLines line : grnLinesList) {
            if (serial == null) {
                tblModel.addRow(new Object[]{"SUP",line.getPcode(),line.getSerial(), line.getCostPrice(),line.getActualCostPrice(),line.getEndUserPrice(),line.getDealerPrice()});

            } else if (line.getSerial().equals(serial)) {

                tblModel.addRow(new Object[]{"SUP",line.getPcode(),line.getSerial(), line.getCostPrice(),line.getActualCostPrice(),line.getEndUserPrice(),line.getDealerPrice()});
            }
        }

    }
    
    private void loadCreditNotedForSupplier(String supplier) {
        Query query = em.createNamedQuery("CreditNote.findByEntityReferance");
        query.setParameter("entityReferance", supplier);
        List<CreditNote> creditNotes = query.getResultList();
        for (CreditNote itm : creditNotes.toArray(new CreditNote[creditNotes.size()])) {
            cbCreditNote.addItem(itm);
        }

    }

